    private void llenarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        
        LSE<Atencion> atenciones = medico.getHistorialConsultas();
        
        modelo.setColumnIdentifiers(new Object[]{
            "CÃ©dula paciente", "Nombre", "Fecha", "Diagnostico"
        });
        
        tablaAtenciones.setModel(modelo);
        
        for(int i = 0; i < atenciones.size(); i++){
            modelo.addRow(new Object[]{
                atenciones.get(i).getCita().getPaciente().getCedula(),
                atenciones.get(i).getCita().getPaciente().getNombre(),
                atenciones.get(i).getCita().getFecha(),
                atenciones.get(i).getDiagnostico()
            });
        }
    }

