    private void btnGestionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGestionesActionPerformed
        // TODO add your handling code here:
        int selec = tablaQuirofanos.getSelectedRow();
        
        if(selec >=0){
            int id = (int) tablaQuirofanos.getModel().getValueAt(selec, 0);
            Separado separado = quirofano.buscarQuirofanoSeparado(id);
            Cirugia cirugia = separado.getCirugia();
            
            VentanaGestionReserva ventana = new VentanaGestionReserva(cirugia);
            VentanaOpcionesAdmin.background.add(ventana);
            ventana.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione una cirug√≠a");
        }
    }//GEN-LAST:event_btnGestionesActionPerformed
    
    private void llenarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        
        LSE<Separado> separados = quirofano.getSeparados();
        modelo.setColumnIdentifiers(new Object[]{
            "ID","Fecha", "Horas"
        });
        
        tablaQuirofanos.setModel(modelo);
        
        for(int i = 0; i < separados.size(); i++){
            Separado separado = separados.get(i);
            modelo.addRow(new Object[]{
                separado.getId(),
                separado.getFecha(),
                separado.getHoraInicio() + "/" + separado.getHoraFinal()
            });
        }
    }

