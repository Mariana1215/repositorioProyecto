    private void btnGenerarAtencionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarAtencionActionPerformed
        // TODO add your handling code here:
        if (pqrsAtencion == null) {
            JOptionPane.showMessageDialog(null, "No hay PQRS por atender");
            return;
        }
        String resp = txtRespuesta.getText();

        if (resp.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese una respuesta a la PQRS");
            return;
        }

        pqrsAtencion.setRespuesta(resp);
        controlador.atenderPQRS(pqrsAtencion);

        JOptionPane.showMessageDialog(null, "PQRS atendida");

        pqrsAtencion = null;
        limpiarCampos();
    }//GEN-LAST:event_btnGenerarAtencionActionPerformed

    private void btnPasarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPasarActionPerformed
        // TODO add your handling code here:
        try {
            if (pqrsAtencion != null) {
                throw new PQRSEnAtencionException();
            } else {
                PQRS peticion = controlador.saltarPQRS();
                Paciente paciente = peticion.getPaciente();

                lblPaciente.setText(paciente.getNombre());
                txtMotivo.setText(peticion.getMotivo());
                controlador.pasarEnProceso(peticion);
                pqrsAtencion = peticion;
                mostrarSiguiente();
            }
        } catch (NoHayPQRSException | PQRSEnAtencionException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnPasarActionPerformed

    private void btnVerMasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerMasActionPerformed
        // TODO add your handling code here:
        VentanaDetallesPQRS ventana = new VentanaDetallesPQRS(pqrsAtencion);
        VentanaOpcionesAdmin.background.add(ventana);
        ventana.setVisible(true);
    }//GEN-LAST:event_btnVerMasActionPerformed

    private void mostrarSiguiente() {
        try {
            PQRS pqrsSiguiente = controlador.mostrarSiguiente();
            Paciente paciente = pqrsSiguiente.getPaciente();
            lblVerAtencion.setText(paciente.getNombre());
            lblFechaAtencion.setText(pqrsSiguiente.getFecha().toString());
        } catch (NoHayPQRSException ex) {
            lblAtencion.setText(ex.getMessage());
            lblFechaAtencion.setText("");

        }
    }

    private void mostrarPQRSAtencion() {
        PQRS pqrs = controlador.mostrarPQRS();
        pqrsAtencion = pqrs;

        if (pqrs != null) {

            Paciente paciente = pqrs.getPaciente();
            lblPaciente.setText(paciente.getNombre());
            txtMotivo.setText(pqrs.getMotivo());
        } else {
            lblPaciente.setText("");
            txtMotivo.setText("");
        }

    }

    private void limpiarCampos() {
        lblPaciente.setText("");
        txtMotivo.setText("");
        txtRespuesta.setText("");
        lblVerAtencion.setText("");
    }

