    private void btnDetallesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetallesActionPerformed
        
        int seleccionado = tablaHistorial.getSelectedRow();

        if (seleccionado >= 0) {
            int id = (int) tablaHistorial.getModel().getValueAt(seleccionado, 0);
            Atencion atencion = controlador.buscarAtencion(id);

            VentanaDetalles ventana = new VentanaDetalles(atencion);
            VentanaOpcionesPaciente.background.add(ventana);
            ventana.setVisible(true);
            this.dispose();

        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una cita");
        }
    }//GEN-LAST:event_btnDetallesActionPerformed

    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        // TODO add your handling code here:
        int seleccion = tablaHistorial.getSelectedRow();
        
        if(seleccion >= 0){
            int id = (int) tablaHistorial.getModel().getValueAt(seleccion, 0);
            Atencion atencion = controlador.buscarAtencion(id);
            
            VentanaGenerarPQRS ventana = new VentanaGenerarPQRS(paciente, atencion);
            VentanaOpcionesPaciente.background.add(ventana);
            ventana.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione una cita");
        }
    }//GEN-LAST:event_btnGenerarActionPerformed
    private void llenarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        
        LSE<Atencion> atenciones = controlador.getAtenciones();
        
        modelo.setColumnIdentifiers(new Object[]{
           "ID", "MÃ©dico", "Fecha", "Hora"
        });
        
        tablaHistorial.setModel(modelo);
        
        for(int i = 0; i < atenciones.size(); i++){
            modelo.addRow(new Object[]{
                atenciones.get(i).getId(),
                atenciones.get(i).getCita().getMedico().getNombre(),
                atenciones.get(i).getCita().getFecha(),
                atenciones.get(i).getCita().getHoraFinal()
                
            });
        }
    }

